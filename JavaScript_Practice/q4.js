var a=10;
console.log(a++);
console.log(++a);
console.log(a)

// start a=10
// 1. a++(Post-increment)
// uses current value first then increment 
// print 10 then a become 11

// 2. ++a(pre-increment)
// increment forst then uses value
// a become 12 print 12
// 3. console.log(a)
// a is already 12 print 12